package inicio;

public class Imagens {

    // Método para imprimir a cara de entediado
    public static char imprimirCaraEntediado() {
        System.out.println("    ┌─────────────┐");
        System.out.println("    │   -    -    │");
        System.out.println("    │      ─      │");
        System.out.println("    │    _____    │");
        System.out.println("    └─────────────┘");
        return 0;
    }

    public static char ImprimirCaraTriste() {
        System.out.println("    ┌─────────────┐");
        System.out.println("    │   ;    ;    │");
        System.out.println("    │      __     │");
        System.out.println("    │   /     \\   │");
        System.out.println("    └─────────────┘");
        return 0;
        }
    }

